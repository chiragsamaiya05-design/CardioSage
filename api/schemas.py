from pydantic import BaseModel

class HeartRequest(BaseModel):
    Age: int
    Sex: str
    ChestPainType: str
    RestingBP: int
    Cholesterol: int
    FastingBS: int
    RestingECG: str
    MaxHR: int
    ExerciseAngina: str
    Oldpeak: float
    ST_Slope: str


class PredictionResponse(BaseModel):
    prediction: str
    probability: float